$(document).ready(function(){
 
    $('span').click({}) //evento de click no elemento
        .dblclick({}) //evento de duplo click no elemento
        .mouseover({}) //evento de colocar o mouse no elemento
        .mouseout({}) //evento de tirar o mouse do elemento
        .mouseup({}) //evento de pressionar o botão do mouse
        .mousedown({}) //evento de soltar o botão do mouse

});